#abs(x) = 절대값구하기
print (abs(-5)) # 5 

#pow(x, y) = x ^ y
print(pow(4, 2)) # 4^2 = 4 * 4 = 16 

#max (x, y) = 최대값 구하기
print(max(5, 12)) # 12

#min (x, y) = 최소값 구하기
print(min(5, 12)) # 5

#round(x) = 반올림
print(round(3.14)) # 3
print(round(4.99)) # 5

#math 라이브러리함에 있는 모든곳을 이용하겠다.
from math import *

#floor(x) = 내림
print(floor(4.99)) #4

#ceil(x) = 올림
print(ceil(3.14)) #4

#sqrt(x) = 제곱근 
print(sqrt(16)) #4 